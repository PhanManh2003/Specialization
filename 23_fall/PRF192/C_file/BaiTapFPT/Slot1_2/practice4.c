#include <stdio.h>
int main(){
    // Declare variables to store the numbers
    int num1 = 2468;
    int num2 = 1234;

    // Display the information
    printf("%d + %d = %d\n", num1, num2, num1 + num2);
    printf("%d - %d = %d\n", num1, num2, num1 - num2);
    printf("%d * %d = %d\n", num1, num2, num1 * num2);
    printf("%d / %d = %d\n", num1, num2, num1 / num2);
	return 0;
}
